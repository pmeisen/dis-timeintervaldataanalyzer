package net.meisen.dissertation.performance.iteration.mock;

/**
 * An instance which has a long as key
 * 
 * @author pmeisen
 * 
 */
public class LongKey {

	final long key;

	/**
	 * Default constructor.
	 * 
	 * @param key
	 *            the key
	 */
	public LongKey(final long key) {
		this.key = key;
	}

	/**
	 * Gets the key.
	 * 
	 * @return the key
	 */
	public long getKey() {
		return key;
	}
}
